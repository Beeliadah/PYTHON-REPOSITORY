# A List is a collection which is ordered and changeable. Allows duplicate members.

# create list
numbers = [1, 2, 3, 4, 5,]
fruits = ['Aples', 'oranges', 'Grapes', 'pears']

# use a constructor
# numbers2 = list(1, 2, 3, 4, 5))

# Get a value
print(fruits[1])

# Get length
print(len[fruits])

# Append to list
fruits.append('mangos')

# Reomove from list
fruits.remove('Grapes')

# Insert into position
fruits.insert(2, 'strawberries')

# Remove with pop
fruits.pop(2)

# Reverse list
fruits.reverse()

# Sort list
fruits.sort()

# Reverse sort
fruits.sort(reverse=True)

print(fruits)